			<h3>Poema</h3>
			<p>
				En este poema se introduce un caso traumático discriminatorio de desigualdad étnica que vive
				una mujer negra a temprana edad. Esta mujer escucha unas voces llamarla “negra” de una
				manera acosadora. Frente a este comentario, ella se siente en negación y rechazo de sí
				misma, de su color y de partes de su cuerpo que la caracterizan debido a su etnia, e intenta
				negar su etnia a través de actos como hacharse polvos y alisarse el pelo. Finalmente,
				termina aceptando sus raíces y su color de piel. Este poema tiene como objetivo demostrar y
				concientizar a las personas de las consecuencias que tienen las palabras y el acoso en la
				vida de los afectados por desigualdad étnica. También crea énfasis en que nos tenemos que
				aceptar de la manera que somos y estar orgullosos de nuestro color, ya que todos somos
				iguales y merecemos el mismo trato.
